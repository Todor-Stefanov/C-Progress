#include <iostream>
using namespace std;


int main()
{
    cout << "12 + 3 = " << 12 + 3 << endl;
    cout << "12 - 2 = " << 12 - 2 << endl;
    cout << "12 / 5 = " << 12 / 5 << endl;  // int - rouding the result
    cout << "12.0 / 5.0 = " << 12.0 / 5.0 << endl;
    cout << "12.0 / 5 = " << 12.0 / 5 << endl;
    cout << "12 % 5 = " << 12 % 5 << endl; // returns the remainder of INT-division
    
    return 0;
}

